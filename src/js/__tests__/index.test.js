import orderByProps from '../forin';

test('проверка сортировки массива', () => {
  const obj = {
    name: 'мечник',
    health: 10,
    level: 2,
    attack: 80,
    defence: 40,
  };
  const arrResult = [
    { key: 'name', value: 'мечник' },
    { key: 'level', value: 2 },
    { key: 'attack', value: 80 },
    { key: 'defence', value: 40 },
    { key: 'health', value: 10 },
  ];
  const start = orderByProps(obj, ['name', 'level']);
  expect(start).toEqual(arrResult);
});
